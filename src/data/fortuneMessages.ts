// comprehensiveFortuneData.ts의 getComprehensiveFortuneData 함수 대체
export function getComprehensiveFortuneData(
  dayElement: Element | string,
  todayElement: Element | string
): ElementFortuneData {
  const elementMap: Record<string, string> = {
    'wood': '목', 'fire': '화', 'earth': '토', 'metal': '금', 'water': '수',
  };
  
  const dayEl = elementMap[dayElement as string] || dayElement;
  const targetEl = elementMap[todayElement as string] || todayElement;
  
  // 템플릿 + 상세 데이터 병합
  const template = createElementFortuneTemplate(dayEl, targetEl);
  const detailed = DETAILED_ELEMENT_FORTUNE[dayEl]?.[targetEl];
  
  if (detailed) {
    return {
      ...template,
      ...detailed,
      overall: { ...template.overall, ...detailed.overall },
    } as ElementFortuneData;
  }
  
  return template;
}

